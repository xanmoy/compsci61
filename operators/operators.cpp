#include<iostream>
using namespace std;
int main(){
    // int a1 = 2 / 5;
    float a1 = 2 / 5;
    cout << a1 << endl;
    cout << 2.0 / 5 << endl;

    int a = 10;
    int b = 20;
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl; // integer division
    cout << "a % b = " << a % b << endl; // remainder
    cout << "a++ = " << a++ << endl; // post increment
    cout << "++a = " << ++a << endl; // pre increment
    cout << "a-- = " << a-- << endl; // post decrement
    cout << "--a = " << --a << endl; // pre decrement
    return 0;
}