#include <stdio.h>

int main()
{
    // This is a single line comment

    printf("Hello World\n");
    printf("I like pizza Its really good\n");
    // escape sequence
    // \n = newline
    // \t = tab
    // \\ = backslash
    // \" = double quote
    // \' = single quote
    printf("1\t2\t3\n4\t5\t6\n7\t8\t9\n");
    printf("\"I like pizza\" - Abraham Lincoin probably\n");
    printf("\'I like pizza\' - Abraham Lincoin probably\n");
    printf("\\I like pizza\\ - Abraham Lincoin probably\n");
    return 0;
}