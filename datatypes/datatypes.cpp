#include<iostream>
using namespace std;
int main(){
    int a = 10; // int is a datatype which stores integer values
    char ch = 'a'; // char is a datatype which stores a single character
    bool b = true; // bool is a datatype which stores boolean values i.e. true or false
    float f = 10.5; // float is a datatype which stores floating point values
    double d = 10.5; // double is a datatype which stores floating point values
    cout << "Integer: " << a << endl;
    cout << "Character: " << ch << endl;
    cout << "Boolean: " << b << endl;
    cout << "Float: " << f << endl;
    cout << "Double: " << d << endl;
    return 0;
}