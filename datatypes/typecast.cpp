#include<iostream>
using namespace std;
int main(){
    // type casting is used to convert one datatype to another
    int a = 'a';
    cout << a << endl;

    char ch = 98;
    cout << ch << endl;

    char ch1 = 123456;
    cout << ch1 << endl;

    unsigned int b = 10;
    cout << b << endl;
    unsigned int c = -10;
}