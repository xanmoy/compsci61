# Data Types in Python
# There are many data types in Python. The most common ones are: 
# - Numbers (integers, floats, complex numbers)
# - Strings (text)
# - Booleans (True or False)
# - None (empty value)


# There are also some other data types that are not used as often:

# - Bytes and Byte Arrays (data represented as bits and bytes)
# - Lists (ordered mutable collection of objects)
# - Tuples (ordered immutable collection of objects)
# - Dictionaries  (unordered collection of key-value pairs)
# - Sets (unordered collection of unique objects)

a= 1
b= "Tanmoy"
c= True
d= None
e= 1.0
f= 1+2j
g= complex(1,2)

print(a)
print(b)
print(c)
print(d)
print(e)
