# There are also some other data types that are not used as often:
# Lists
list1= [1,"xanmoy",3,True,5]
print(list1)
print(type(list1))

# Tuples
tuple1= (1,"xanmoy",3,True,5)
print(tuple1)
print(type(tuple1))

# Dictionaries
dict1= {"name":"Tanmoy","age":25,"city":"Kolkata"}
print(dict1)
print(type(dict1))

# Sets
set1= {1,"xanmoy",3,True,5}
print(set1)
print(type(set1))

# Bytes and Byte Arrays
byte1= b"Hello"
print(byte1)
